// ─── Greeting helper ──────────────────────────────────────────────────────────
export function getGreeting(): string {
  const h = new Date().getHours();
  if (h < 12) return T.home.greeting;
  if (h < 17) return T.home.greetingAfternoon;
  return T.home.greetingEvening;
}
